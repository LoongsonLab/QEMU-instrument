static std::string ljstr(const std::string& s, UINT32 width, CHAR padding = ' ')
{
    std::string ostr(width, padding);
    ostr.replace(0, s.length(), s);
    return ostr;
}

static std::string rjstr(const std::string& s, UINT32 width, CHAR padding = ' ')
{
    if (s.size() < width)
        return std::string(width - s.size(), padding) + s;
    else
        return s;


}
static std::string fltstr(FLT64 val, UINT32 prec = 0, UINT32 width = 0)
{
    std::ostringstream o;
    o << std::fixed << std::setprecision(prec);
    if(width > 0)
        o << std::setw(width);
    o << val;
    std::string str(o.str());
    return str;
}

static std::string decstr(UINT32 val, UINT32 width = 0)
{
    std::ostringstream o;

    if(width > 0)
        o << std::setw(width) << std::setfill(' ');

    o << val;
    std::string str(o.str());
    return str;
}

static std::string hexstr(UINT64 val, UINT32 width = 0)
{
    std::ostringstream o;

    o << "0x";
    if (width > 0)
    {
        width = (width > 2) ? (width - 2) : 0;
        o << std::setw(width) << std::setfill('0');
    }

    o << std::hex << val;

    std::string str(o.str());
    return str;
}

